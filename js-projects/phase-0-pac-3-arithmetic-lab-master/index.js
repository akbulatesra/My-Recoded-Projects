const makeInt = (n) => {
  const parsedInt = parseInt(n, 10);
  return parsedInt;
};
const preserveDecimal = (n) => {
  const parsedFloat = parseFloat(n);
  return parsedFloat;
};
const add = (a, b) => {
  const result = a + b;
  return result;
};
const subtract = (a, b) => {
  const result = a - b;
  return result;
};
const multiply = (a, b) => {
  const result = a * b;
  return result;
};
const divide = (a, b) => {
  const result = a / b;
  return result;
};
const increment = (n) => {
  const result = n + 1;
  return result;
};
const decrement = (n) => {
  const result = n - 1;
  return result;
};
